#include <iostream>
#include <cmath>
using namespace std;

int main(){
int x = 7, y = 9, z = 11;
cout << max(10, 17);//Finds the highest value of x and y
cout << min(10 ,17);//Find the lowest value of x and y
cout << sqrt(64);//Find the square root method built-in cmath
cout << round(2.6);//Rounds a number
cout << log(2);//Natural logarithm
cout << abs(x);//Absolute value of x
cout << acos(x);//arccosine of x
cout << asin(x);//Arcsine of x
cout << atan(x);//Arctangent of x
cout << cbrt(x);//Cuberoot of x
cout << ceil(x);//Up Nearest integer of x
cout << cos(x);//Cosine of x
cout << cosh(x);//Hyperbolic cosine of x
cout << exp(x);//E^x
cout << expm1(x);//e^x-1
cout << fabs(x);//Absolute value of a floating x
cout << fdim(x, y);//Positive difference between x and y
cout << floor(x);//Down Nearest Integer of x
cout << hypot(x, y);//Square addition without intermediate overflow or underflow
cout << fma(x, y, z);//x*y+z without losing precision
cout << fmax(x, y);//Highest value of a floating x and y
cout << fmin(x, y);//Lowest value of a floating x and y
cout << fmod(x,y);//Floating point remainder of xy
cout << pow(x,y);//x to the power of y
cout << sin(x);//sine of x (x is in radians)
cout << sinh(x);//hyperbolic sine of a double value
cout << tan(x);//tangent of an angle
cout << tanh(x);//hyperbolic tangent of a double value
return 0;
}
